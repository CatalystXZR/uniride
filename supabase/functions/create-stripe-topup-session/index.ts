// supabase/functions/create-stripe-topup-session/index.ts
// Stripe-ready endpoint contract. Returns 501 until keys/SDK are wired.

import { serve } from "https://deno.land/std@0.177.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

function jsonResponse(payload: unknown, status = 200) {
  return new Response(JSON.stringify(payload), {
    status,
    headers: { ...corsHeaders, "Content-Type": "application/json" },
  });
}

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  if (req.method !== "POST") {
    return jsonResponse({ error: "method_not_allowed" }, 405);
  }

  return jsonResponse(
    {
      error: "not_implemented",
      message: "Stripe session endpoint scaffold ready. Connect Stripe secret key and SDK to enable.",
    },
    501,
  );
});
